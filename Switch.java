package day1;

public class Switch {

	public static void main(String[] args) {
		
		String x = " ni ";
		switch (x){
		case "one":
			System.out.println("value is 0");
			//break;
		case "two":
			System.out.println("value is 1");
			//break;
		case "dd":
			System.out.println("value is 2");
			break;
		case "ff":
			System.out.println("value is 3");
			break;
			default:
			System.out.println("no match");
		}

	}

}
